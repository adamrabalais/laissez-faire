// --- ALGORITHM Helpers ---
const generateShoppingList = (plan: any[], peopleCount: number) => {
  const list: any = {};
  
  plan.forEach((recipe: any) => {
    const scaleFactor = peopleCount / (recipe.servings || 4);

    recipe.ingredients.forEach((ing: any) => {
      const scaledAmount = ing.amount * scaleFactor;

      if (!list[ing.name]) {
        list[ing.name] = { 
          name: ing.name,
          totalAmount: scaledAmount, 
          unit: ing.unit, 
          category: ing.category, 
          emoji: ing.emoji || 'ðŸ¥˜', 
          recipes: [recipe.title],
          count: 1 
        };
      } else {
        if (list[ing.name].unit === ing.unit) {
          list[ing.name].totalAmount += scaledAmount;
        }
        list[ing.name].count += 1;
        list[ing.name].recipes.push(recipe.title);
      }
    });
  });

  return Object.values(list).sort((a: any, b: any) => a.category.localeCompare(b.category));
};

const formatAmount = (amount: number) => {
  if (amount % 1 === 0) return amount;
  return amount.toFixed(1).replace('.0', '');
};

// Helper to extract domain name for display
const getDomain = (url: string) => {
  try {
    const domain = new URL(url).hostname;
    return domain.replace('www.', '').split('.')[0]; // e.g. "allrecipes"
  } catch (e) {
    return 'Web';
  }
};

